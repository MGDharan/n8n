public class FibonacciSequence {
    public static void main(String[] args) {
        int n = 10; // Number of Fibonacci numbers to generate
        long a = 0, b = 1; // Initialize first two Fibonacci numbers

        System.out.print("Fibonacci Sequence: ");
        for (int i = 0; i < n; i++) {
            System.out.print(a + " ");
            long temp = a + b;
            a = b;
            b = temp;
        }
        System.out.println();
    }
}